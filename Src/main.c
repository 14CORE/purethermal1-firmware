int main(void)
{

  /* USER CODE BEGIN 1 */
  int i;

  // IRE = 0
  memset(gpData, GPIO_PIN_0, sizeof(gpData));

  // SYNC
  for (i=0; i<34; i++)
  {
    gpData[i] &= ~GPIO_PIN_0;
  }

  // color burst
  for (i=38; i<(38+18); i++)
  {
    if ((i % 2) == 0)
      gpData[i] |= GPIO_PIN_6;
  }

  memcpy(gpData2, gpData, sizeof(gpData));

  for (i=0; i<455; i++)
  {
    arrData[i] = 14;
  }

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_CRC_Init();
  MX_I2C1_Init();
  MX_SPI2_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  MX_USART2_UART_Init();
  MX_USB_DEVICE_Init();

  /* USER CODE BEGIN 2 */
  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_RESET);


  DEBUG_PRINTF("Hello, Lepton!\n\r");
  fflush(stdout);

  lepton_init();

  HAL_Delay(1000);

  init_lepton_command_interface();
#ifdef ENABLE_LEPTON_AGC
  enable_lepton_agc();
#endif

#ifdef Y16
  enable_telemetry();
#else
  enable_rgb888(PSUEDOCOLOR_LUT);
#endif

  DEBUG_PRINTF("reading_tmp007_regs...\n\r");

  read_tmp007_regs();

  DEBUG_PRINTF("Initialized...\n\r");

  HAL_Delay(250);

  PT_INIT(&lepton_task_pt);
  PT_INIT(&usb_task_pt);
  PT_INIT(&uart_task_pt);
  PT_INIT(&video_task_pt);

  PT_SEM_INIT(&gpDataSem, 0);

  // only generate update on overflow
  htim1.Instance->CR1 &= ~TIM_CR1_URS;
  htim3.Instance->CR1 &= ~TIM_CR1_URS;

  /* Enable the Output compare channel */
  TIM_CCxChannelCmd(htim1.Instance, TIM_CHANNEL_1, TIM_CCx_ENABLE);
  
  if(IS_TIM_ADVANCED_INSTANCE(htim1.Instance) != RESET)
  {
    /* Enable the main output */
    __HAL_TIM_MOE_ENABLE(&htim1);
  }

  htim1.hdma[TIM_DMA_ID_UPDATE]->XferCpltCallback = TIM_DMADelayPulseCplt;
  htim1.hdma[TIM_DMA_ID_UPDATE]->XferErrorCallback = TIM_DMAError;

  htim1.hdma[TIM_DMA_ID_CC1]->XferCpltCallback = TIM_DMADelayPulseCplt;
  htim1.hdma[TIM_DMA_ID_CC1]->XferErrorCallback = TIM_DMAError;

  reset_tim1();

  WHITE_LED_TOGGLE;

  __HAL_TIM_ENABLE(&htim3);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

    // PT_SCHEDULE(lepton_task(&lepton_task_pt));
    // PT_SCHEDULE(usb_task(&usb_task_pt));
    // PT_SCHEDULE(uart_task(&uart_task_pt));
    // PT_SCHEDULE(button_task(&button_task_pt));
    PT_SCHEDULE(video_task(&video_task_pt));

  }
  /* USER CODE END 3 */

}

